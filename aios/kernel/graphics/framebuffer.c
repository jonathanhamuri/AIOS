void fb_putpixel(int x, int y, unsigned int color) {
    if(!fb_addr||x<0||x>=(int)fb_width||y<0||y>=(int)fb_height) return;
    unsigned int* fb = (unsigned int*)(fb_addr + y*fb_pitch + x*4);
    *fb = color;
}

void fb_clear(unsigned int color) {
    if(!fb_addr) return;
    unsigned int* fb = (unsigned int*)fb_addr;
    unsigned int pixels = fb_pitch/4 * fb_height;
    for(unsigned int i=0;i<pixels;i++) fb[i]=color;
}

void fb_rectfill(int x, int y, int w, int h, unsigned int color) {
    for(int j=y;j<y+h;j++)
        for(int i=x;i<x+w;i++)
            fb_putpixel(i,j,color);
}

void fb_drawchar(int x, int y, char c, unsigned int fg, unsigned int bg) {
    if(c<32||c>127) c=32;
    unsigned char* g = fb_font[c-32];
    for(int row=0;row<8;row++)
        for(int col=0;col<8;col++)
            fb_putpixel(x+col,y+row,(g[row]&(0x80>>col))?fg:bg);
}

void fb_drawstring(int x, int y, const char* s, unsigned int fg, unsigned int bg) {
    int cx=x;
    while(*s){
        if(*s=='\n'){cx=x;y+=10;}
        else{fb_drawchar(cx,y,*s,fg,bg);cx+=8;}
        s++;
    }
}

void fb_drawint(int x, int y, int val, unsigned int fg, unsigned int bg) {
    char buf[12]; int i=0;
    if(val==0){buf[i++]='0';}
    while(val>0){buf[i++]='0'+val%10;val/=10;}
    for(int a=0,b=i-1;a<b;a++,b--){char t=buf[a];buf[a]=buf[b];buf[b]=t;}
    buf[i]=0;
    fb_drawstring(x,y,buf,fg,bg);
}

int fb_init() {
    multiboot_info_t* mbi = (multiboot_info_t*)mbi_ptr;
    if(!mbi) return -1;

    // Check if framebuffer info is present (bit 12)
    if(!(mbi->flags & (1<<12))) {
        terminal_print_color("FB: No framebuffer in multiboot\n",
            MAKE_COLOR(COLOR_BRED,COLOR_BLACK));
        return -1;
    }

    fb_addr   = (unsigned int)mbi->framebuffer_addr;
    fb_width  = mbi->framebuffer_width;
    fb_height = mbi->framebuffer_height;
    fb_pitch  = mbi->framebuffer_pitch;
    fb_bpp    = mbi->framebuffer_bpp;

    if(fb_bpp != 32) {
        terminal_print_color("FB: Need 32bpp\n",
            MAKE_COLOR(COLOR_BYELLOW,COLOR_BLACK));
        return -1;
    }

    fb_active = 1;

    terminal_print_color("Framebuffer      : OK (",
        MAKE_COLOR(COLOR_BCYAN,COLOR_BLACK));
    terminal_print_int(fb_width);
    terminal_print_color("x",MAKE_COLOR(COLOR_BCYAN,COLOR_BLACK));
    terminal_print_int(fb_height);
    terminal_print_color(")\n",MAKE_COLOR(COLOR_BCYAN,COLOR_BLACK));

    return 0;
}

void fb_shell_newline() {
    fb_cursor_x = 4;
    fb_cursor_y += 12;
    int limit = (int)fb_height - 40;
    if(fb_cursor_y > limit) {
        // Scroll up
        unsigned char* fb = (unsigned char*)fb_addr;
        for(int y=24;y<limit;y++)
            for(int x=0;x<(int)fb_pitch;x++)
                fb[y*(int)fb_pitch+x] = fb[(y+12)*(int)fb_pitch+x];
        fb_rectfill(0,limit-12,fb_width,12,FB_BLACK);
        fb_cursor_y = limit-12;
    }
}

void fb_shell_print(const char* s, unsigned int color) {
    while(*s){
        if(*s=='\n') fb_shell_newline();
        else{
            fb_drawchar(fb_cursor_x,fb_cursor_y,*s,color,FB_BLACK);
            fb_cursor_x+=8;
            if(fb_cursor_x>(int)fb_width-10) fb_shell_newline();
        }
        s++;
    }
}

void fb_shell_prompt() {
    int bar_y = fb_height - 30;
    fb_rectfill(0,bar_y,fb_width,30,FB_DGRAY);
    // Green line separator
    fb_rectfill(0,bar_y,fb_width,1,0x0000AA00);
    // Ubuntu-style prompt
    fb_drawstring(4,bar_y+8,"aios",0x0000FF00,FB_DGRAY);      // green
    fb_drawstring(36,bar_y+8,"@",FB_WHITE,FB_DGRAY);
    fb_drawstring(44,bar_y+8,"system",0x0000FFFF,FB_DGRAY);   // cyan
    fb_drawstring(92,bar_y+8,":",FB_WHITE,FB_DGRAY);
    fb_drawstring(100,bar_y+8,"~$",FB_WHITE,FB_DGRAY);
    fb_input_x = 124;
    // Draw cursor
    fb_rectfill(fb_input_x,bar_y+6,8,14,FB_WHITE);
}

void fb_shell_init() {
    fb_clear(FB_BLACK);
    // Title bar - Ubuntu purple
    fb_rectfill(0,0,fb_width,22,0x00300030);
    fb_drawstring(fb_width/2-70,6,"AIOS Terminal - AI Operating System",FB_WHITE,0x00300030);
    // Separator line
    fb_rectfill(0,22,fb_width,1,0x00444444);
    fb_cursor_x = 4;
    fb_cursor_y = 28;
    fb_shell_prompt();
}

void fb_scroll_up() {
    unsigned char* fb=(unsigned char*)fb_addr;
    int limit = fb_height-40;
    for(int y=24;y<limit;y++)
        for(int x=0;x<(int)fb_pitch;x++)
            fb[y*(int)fb_pitch+x]=fb[(y+12)*(int)fb_pitch+x];
    fb_rectfill(0,limit-12,fb_width,12,FB_BLACK);
}

void fb_scroll_down() {
    unsigned char* fb=(unsigned char*)fb_addr;
    for(int y=fb_height-52;y>=36;y--)
        for(int x=0;x<(int)fb_pitch;x++)
            fb[y*(int)fb_pitch+x]=fb[(y-12)*(int)fb_pitch+x];
    fb_rectfill(0,24,fb_width,12,FB_BLACK);
}
