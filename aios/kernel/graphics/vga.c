static void outb(unsigned short port, unsigned char val) {
    __asm__ volatile("outb %0,%1"::"a"(val),"Nd"(port));
}
static unsigned char inb(unsigned short port) {
    unsigned char v;
    __asm__ volatile("inb %1,%0":"=a"(v):"Nd"(port));
    return v;
}

