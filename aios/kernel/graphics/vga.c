void vga_putpixel(int x, int y, unsigned char color) {
    if(x<0||x>=VGA_WIDTH||y<0||y>=VGA_HEIGHT) return;
    unsigned char* fb = (unsigned char*)VGA_MEMORY;
    fb[y*VGA_WIDTH+x] = color;
}

void vga_clear(unsigned char color) {
    unsigned char* fb = (unsigned char*)VGA_MEMORY;
    for(int i=0;i<VGA_WIDTH*VGA_HEIGHT;i++) fb[i]=color;
}

void vga_rectfill(int x, int y, int w, int h, unsigned char color) {
    for(int j=y;j<y+h;j++)
        for(int i=x;i<x+w;i++)
            vga_putpixel(i,j,color);
}

void vga_rect(int x, int y, int w, int h, unsigned char color) {
    for(int i=x;i<x+w;i++){vga_putpixel(i,y,color);vga_putpixel(i,y+h-1,color);}
    for(int j=y;j<y+h;j++){vga_putpixel(x,j,color);vga_putpixel(x+w-1,j,color);}
}

void vga_line(int x0,int y0,int x1,int y1,unsigned char color){
    int dx=x1-x0; if(dx<0)dx=-dx;
    int dy=y1-y0; if(dy<0)dy=-dy;
    int sx=x0<x1?1:-1,sy=y0<y1?1:-1,err=dx-dy;
    while(1){
        vga_putpixel(x0,y0,color);
        if(x0==x1&&y0==y1)break;
        int e2=2*err;
        if(e2>-dy){err-=dy;x0+=sx;}
        if(e2<dx){err+=dx;y0+=sy;}
    }
}

void vga_drawchar(int x, int y, char c, unsigned char fg, unsigned char bg) {
    if(c<32||c>127) c=32;
    unsigned char* g = font8x8[c-32];
    for(int row=0;row<8;row++)
        for(int col=0;col<8;col++)
            vga_putpixel(x+col,y+row,(g[row]&(0x80>>col))?fg:bg);
}

void vga_drawstring(int x, int y, const char* s, unsigned char fg, unsigned char bg){
    int cx=x;
    while(*s){
        if(*s=='\n'){cx=x;y+=9;}
        else{vga_drawchar(cx,y,*s,fg,bg);cx+=8;}
        s++;
    }
}

void vga_drawint(int x, int y, int val, unsigned char fg, unsigned char bg){
    char buf[12]; int i=0;
    if(val==0){buf[i++]='0';}
    while(val>0){buf[i++]='0'+val%10;val/=10;}
    for(int a=0,b=i-1;a<b;a++,b--){char t=buf[a];buf[a]=buf[b];buf[b]=t;}
    buf[i]=0;
    vga_drawstring(x,y,buf,fg,bg);
}

void vga_init() {
    outb(0x3C2,0x63);
    unsigned char seq[]={0x03,0x01,0x0F,0x00,0x0E};
    for(int i=0;i<5;i++){outb(0x3C4,i);outb(0x3C5,seq[i]);}
    outb(0x3D4,0x11);outb(0x3D5,inb(0x3D5)&0x7F);
    unsigned char crtc[]={
        0x5F,0x4F,0x50,0x82,0x54,0x80,0xBF,0x1F,
        0x00,0x41,0x00,0x00,0x00,0x00,0x00,0x00,
        0x9C,0x0E,0x8F,0x28,0x40,0x96,0xB9,0xA3,0xFF
    };
    for(int i=0;i<25;i++){outb(0x3D4,i);outb(0x3D5,crtc[i]);}
    unsigned char gc[]={0x00,0x00,0x00,0x00,0x00,0x40,0x05,0x0F,0xFF};
    for(int i=0;i<9;i++){outb(0x3CE,i);outb(0x3CF,gc[i]);}
    inb(0x3DA);
    unsigned char ac[]={
        0x00,0x01,0x02,0x03,0x04,0x05,0x06,0x07,
        0x08,0x09,0x0A,0x0B,0x0C,0x0D,0x0E,0x0F,
        0x41,0x00,0x0F,0x00,0x00
    };
    for(int i=0;i<21;i++){outb(0x3C0,i);outb(0x3C0,ac[i]);}
    outb(0x3C0,0x20);
    vga_active=1;
    terminal_print_color("Graphics         : OK (320x200)\n",
        MAKE_COLOR(COLOR_BCYAN,COLOR_BLACK));
}

void vga_shell_init() {
    vga_clear(COL_BLACK);
    vga_rectfill(0,0,320,10,5);
    vga_drawstring(70,1,"AIOS Terminal",COL_WHITE,5);
    vga_line(0,10,319,10,COL_DGRAY);
    vga_rectfill(0,182,320,18,COL_DGRAY);
    vga_line(0,182,319,182,COL_GREEN);
    vga_drawstring(2,186,"aios",COL_GREEN,COL_DGRAY);
    vga_drawstring(34,186,"@",COL_WHITE,COL_DGRAY);
    vga_drawstring(42,186,"system",COL_CYAN,COL_DGRAY);
    vga_drawstring(90,186,":",COL_WHITE,COL_DGRAY);
    vga_drawstring(98,186,"~$",COL_WHITE,COL_DGRAY);
    input_cursor_x=122;
    vga_rectfill(122,185,6,10,COL_WHITE);
    shell_cursor_x=2;
    shell_cursor_y=13;
}
void vga_shell_newline() {
    shell_cursor_x=2;
    shell_cursor_y+=9;
    if(shell_cursor_y>172){
        unsigned char* fb=(unsigned char*)VGA_MEMORY;
        for(int y=12;y<172;y++)
            for(int x=0;x<320;x++)
                fb[y*320+x]=fb[(y+9)*320+x];
        vga_rectfill(0,163,320,9,COL_BLACK);
        shell_cursor_y=163;
    }
}
void vga_shell_print(const char* s, unsigned char color) {
    while(*s){
        if(*s=='\n') vga_shell_newline();
        else{
            vga_drawchar(shell_cursor_x,shell_cursor_y,*s,color,COL_BLACK);
            shell_cursor_x+=8;
            if(shell_cursor_x>312) vga_shell_newline();
        }
        s++;
    }
}
